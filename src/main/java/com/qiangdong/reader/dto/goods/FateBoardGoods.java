package com.qiangdong.reader.dto.goods;

public class FateBoardGoods {
    private String upAppearance = "";
    private String downAppearance = "";
    private String name = "";

    public String getUpAppearance() {
        return upAppearance;
    }

    public void setUpAppearance(String upAppearance) {
        this.upAppearance = upAppearance;
    }

    public String getDownAppearance() {
        return downAppearance;
    }

    public void setDownAppearance(String downAppearance) {
        this.downAppearance = downAppearance;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
